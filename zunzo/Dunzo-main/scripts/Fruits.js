getfruits.map(function(data){
    var box = document.createElement("div");
    box.setAttribute("id" , "box")


    var link = document.createElement("a");
    link.setAttribute("href" , "data.url");

   var box_inside = document.createElement("div");
   box_inside.setAttribute("id" , "box_inside");

   var image = document.createElement("img");
   image.setAttribute("src" , data.image_url);
   image.setAttribute("id" , "image");

   var text = document.createElement("div");
   text.setAttribute("id" , "text");

   var name = document.createElement("h3");
   name.textContent = data.name;

   var address = document.createElement("p");
   address.textContent = data.address;

   var distance = document.createElement("p");
   distance.textContent = data.distance;

   text.append(name, address, distance);

   box_inside.append(image , text);

   box.append(link,box_inside, text);

   document.getElementById("main").append(box);
});

document.querySelector("#box").addEventListener("click" , newPage);

